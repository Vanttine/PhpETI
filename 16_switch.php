<?php 

$number = 10;

switch ($number) {

    case $number == 12;
        echo 'Doze';

    break;

    case $number == 13;
        echo 'Treze';

    break;

    case $number == 14;
        echo 'Quatorze';

    break;

    case $number == 15;
        echo 'Quinze';

    break;

    default:
        echo 'Nao identificado...';

        break;
}